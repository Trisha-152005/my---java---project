package com.Trisha;


	public class ArraySum {
	    public static void main(String[] args) {
	        int[] numbers = {5, 12, 7, 3, 9}; // Sample array
	        int sum = 0;

	        for (int num : numbers) {
	            sum += num; // Add each element to the sum
	        }

	        System.out.println("Sum of all elements: " + sum);
	    }
	}

